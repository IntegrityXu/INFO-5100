    public void refreshTable() {
        int rowCount = userJTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)userJTable.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Organization og : musicPlatEnterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount us : og.getUserAccountDirectory().getUserAccountList()) {
                Object [] row = new Object[2];
                row[0] = us;
                row[1] = us.getRole();
                model.addRow(row);
            }
        }
        nameTxt.setText("");
        pwdTxt.setText("");
    }
    
    public void populateCombox() {
        roleCbx.removeAllItems();
        for (Role role : musicPlatEnterprise.getSupportRoles()) {
            roleCbx.addItem(role);
        }
    }
    
