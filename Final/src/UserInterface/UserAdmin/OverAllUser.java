    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        //创建角色
        
        if(nameTxt.getText().equals("") || pwdTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "The account name and password can't be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Role role = (Role)roleCbx.getSelectedItem();
        String name = nameTxt.getText();
        for (Organization og : musicPlatEnterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : og.getUserAccountDirectory().getUserAccountList()) {
                if (ua.getUserName().equals(name)) {
                    JOptionPane.showMessageDialog(null, "this Account has exist !!", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
        }
        
        UserAccount ua = null;
        if ( role instanceof CustomerRole) {
            ua = new CustomerAccount(name, pwdTxt.getText(), role);
        }
        else
            ua = new UserAccount(name, pwdTxt.getText(), role);
        
        Organization organization = null;
        
        for (Organization og : musicPlatEnterprise.getOrganizationDirectory().getOrganizationList()) {
            if ( og.getSupportRoles().get(0).equals(role) ){
                organization = og;
                break;
            }
        }
        
        if (organization == null) {
            JOptionPane.showMessageDialog(null, "no such organization ! ");
            return;
        }
        organization.getUserAccountDirectory().AddUserAccount(ua);
        JOptionPane.showMessageDialog(null, "Create Account successfully!");
        refreshTable();
    }//GEN-LAST:event_createUserJButtonActionPerformed

