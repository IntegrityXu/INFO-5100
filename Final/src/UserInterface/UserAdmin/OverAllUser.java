    private void viewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = userJTable.getSelectedRow();
        if( selectRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select one row from the table!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        UserAccount ua = (UserAccount)userJTable.getValueAt(selectRow, 0);
        ViewUserDetail vudJPanel = new ViewUserDetail(container, ua);
        container.add("ViewUsrDetail", vudJPanel);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_viewDetailsBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Are you sure to delete this acccount?", "Warning", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.NO_OPTION) {
            return;
        }
        int selectRow = userJTable.getSelectedRow();
        if( selectRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select one row from the table!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        UserAccount ua = (UserAccount)userJTable.getValueAt(selectRow, 0);
        for (Organization og : musicPlatEnterprise.getOrganizationDirectory().getOrganizationList()) {
            System.out.println(og.getSupportRoles().get(0));
            if (og.getSupportRoles().get(0).equals(ua.getRole())) {
                og.getUserAccountDirectory().RemoveUserAccount(ua);
            }
        }
        
        
        //System.out.println(a);
        JOptionPane.showMessageDialog(null, "Delete Account successfully!");
        refreshTable();
    }//GEN-LAST:event_deleteBtnActionPerformed


