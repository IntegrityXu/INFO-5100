    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        container.remove(this);
        CardLayout layout = (CardLayout)container.getLayout();
        layout.previous(container);
        Component[] components = container.getComponents();
        for (Component cp : components) {
            if (cp instanceof OverAllUser) {
                ((OverAllUser) cp).refreshTable();
            }
        } 
    }//GEN-LAST:event_backBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if (nameTxt.getText().equals("") || pwdTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "the account name and password can't be empty!");
            return;
        }
        if (ua instanceof CustomerAccount) {
            CustomerAccount ca = (CustomerAccount) ua;
            if (vipCbx.getSelectedItem().equals("VIP"))
                ca.setVip(true);
            else 
                ca.setVip(false);
            }
            
        ua.setUserPwd(pwdTxt.getText());
        ua.setUserName(nameTxt.getText());   
        JOptionPane.showMessageDialog(null, "Updated successfully");  
        populate();
        vipCbx.setEnabled(false);
    }//GEN-LAST:event_saveBtnActionPerformed


