    public void populateCbx( ) {
        vipCbx.removeAllItems();
        if (ua instanceof CustomerAccount) {
            vipCbx.addItem("VIP");
            vipCbx.addItem("No VIP");
            if (((CustomerAccount)ua).isVip()) {
                vipCbx.setSelectedItem("VIP");
            }
            else
                vipCbx.setSelectedItem("No VIP");
        }
        else 
        {
            vipCbx.addItem("");
        }
        vipCbx.setEnabled(false);
    }

