    private void CreateSongBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateSongBtnActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("mm:ss");
        try {
            Integer.parseInt(singerIDTxt.getText());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "the SignerID must be Integer !!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            sdf.parse(TimeTxt.getText());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "The time format must be \"mm:ss\" !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        int singerID = Integer.parseInt(singerIDTxt.getText());
        String singerName = singerDirectory.getNameFromeID(singerID);
        
        if (singerName == null) {
            JOptionPane.showMessageDialog(null, "Please add the singer first!!", "Waring", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Song song = new Song(NameTxt.getText(), TimeTxt.getText(), singerID, albumTxt.getText());
        song.setSinger(singerName);
        
        int result = songDirectory.AddSong(song);
        if (result == 0) {
            Song.count --;
            JOptionPane.showMessageDialog(null, "this song has exist!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null, "Add a song successfully!");
        
    }//GEN-LAST:event_CreateSongBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
        
        Component [] components = container.getComponents();
        for( Component cp : components) {
            if(cp instanceof MusicLibrary) {
                MusicLibrary ml = (MusicLibrary) cp;
                ml.refreshTable();
            }
        }
    }//GEN-LAST:event_backBtnActionPerformed


