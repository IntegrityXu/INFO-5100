    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        // TODO add your handling code here:
        if (messageTxtArea.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "The message can't be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String message = messageTxtArea.getText();
        SongOrganization og = new SongOrganization();
        ChangeSongRequest request = new ChangeSongRequest(account, og, message, songID);
        request.setStatus(WorkRequest.states.Finish.getValue());
        network.getWorkQueue().AddWorkrequest(request);
        
    }//GEN-LAST:event_sendBtnActionPerformed

