    public void refreshTable() {
        int rowCount = singerListTbl.getRowCount();
        DefaultTableModel model = (DefaultTableModel)singerListTbl.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Singer singer : singerDirectory.getSingerList()) {
            Object row[] = new Object[2];
            row[0] = singer;//why
            row[1] = singer.getId();
            model.addRow(row);
        }
    }
