    private void singerListDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_singerListDeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = singerListTbl.getSelectedRow();

        if(row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Singer singer = (Singer)singerListTbl.getValueAt(row, 0);
        int singerId = singer.getId();
        
        singerDirectory.RemoveSinger(singer.getId());
        Iterator <Song> iterator = songDirectory.getSongList().iterator();
        while (iterator.hasNext()) {
            Song song = iterator.next();
            if (song.getSingerid() == singerId) {
                iterator.remove();
            }
        }
        refreshTable();
    }//GEN-LAST:event_singerListDeleteBtnActionPerformed

