    public void populateTbl () {
        int rowCount = songTbl.getRowCount();
        DefaultTableModel model = (DefaultTableModel) songTbl.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (Enterprise ep : network.getEnterpriseDirectory().getEnterpaiseList()) {
            if (ep instanceof RecordCompanyEnterprise) {
                for (Organization og : ep.getOrganizationDirectory().getOrganizationList()) {
                    for (Song song : og.getSongDirectory().getSongList()) {
                        Object[] row = new Object[3];
                        row[0] = song;
                        row[1] = song.getId();
                        row[2] = song.getSinger();
                        model.addRow(row);
                    }
                }
                return;
            }
        }
    }
