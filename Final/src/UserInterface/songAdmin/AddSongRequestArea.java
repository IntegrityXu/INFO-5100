    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        // TODO add your handling code here:
        RecordCompanyEnterprise recordCompany = null;
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpaiseList()) {
            if(enterprise instanceof RecordCompanyEnterprise) {
                recordCompany = (RecordCompanyEnterprise)enterprise;
                break;
            }
        }
        if (recordCompany == null) {
            JOptionPane.showMessageDialog(null, "No Record Company !", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            Integer.parseInt(songIdTxt.getText());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "The SongId must be Integer! ", "Warnint", JOptionPane.WARNING_MESSAGE);
            songIdTxt.setText("");
            return;
        }
        AddSongRequest addSongRequest = new AddSongRequest(musicPlatEnterprise, sender, recordCompany, "Add A Song. ", Integer.parseInt(songIdTxt.getText()));
        network.getWorkQueue().AddWorkrequest(addSongRequest);
        JOptionPane.showMessageDialog(null, "Work Request send successfully");
        songIdTxt.setText("");
    }//GEN-LAST:event_sendBtnActionPerformed


