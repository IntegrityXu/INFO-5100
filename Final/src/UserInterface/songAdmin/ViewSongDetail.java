    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        container.remove(this);
        CardLayout layout = (CardLayout)container.getLayout();
        layout.previous(container);
        Component[] component = container.getComponents();
        for ( Component c : component ) {
            if ( c instanceof DealWithRequestArea) {
                DealWithRequestArea dwraJP = (DealWithRequestArea) c;
                dwraJP.Populate();
            }
            if ( c instanceof OverAllSong) {
                OverAllSong oasJP = (OverAllSong) c;
                oasJP.populateTabel();
            }
        }
        
    }//GEN-LAST:event_backBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if (nameTxt.getText().equals("") || timeTxt.getText().equals("") || singerTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "please input all fields!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        song.setVip((vipCbx.getSelectedItem().equals("VIP")));
        song.setName(nameTxt.getText());
        song.setTime(timeTxt.getText());
        song.setSinger(singerTxt.getText());
        
        if (changeSongRequest != null) {
            changeSongRequest.setStatus(WorkRequest.states.Finish.getValue());
            Date date = new Date();
            changeSongRequest.setResolveDate(date);
            
        }

        JOptionPane.showMessageDialog(null, "Updated successfully");
        populate();
    }//GEN-LAST:event_saveBtnActionPerformed


