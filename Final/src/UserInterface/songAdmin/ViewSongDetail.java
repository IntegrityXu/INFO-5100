    public void populate () {
        nameTxt.setText(song.getName());
        timeTxt.setText(song.getTime());
        singerTxt.setText(song.getSinger());
        IDtxt.setText(String.valueOf(song.getId()));
        vipCbx.removeAllItems();
        vipCbx.addItem("VIP");
        vipCbx.addItem("unVIP");
        if (song.isVip())
            vipCbx.setSelectedItem("VIP");
        else
            vipCbx.setSelectedItem("unVIP");
        vipCbx.setEnabled(false);
        
        nameTxt.setEditable(false);
        nameTxt.setEnabled(false);
        timeTxt.setEditable(false);
        timeTxt.setEnabled(false);
        IDtxt.setEditable(false);
        IDtxt.setEnabled(false);
        singerTxt.setEditable(false);
        singerTxt.setEnabled(false);
        saveBtn.setEnabled(false);
        UpdateBtn.setEnabled(true);
        
        if(changeSongRequest == null) {
            mess.setText("");
            
        }
        else {
            mess.setText(changeSongRequest.getMassage());
        }
    }
