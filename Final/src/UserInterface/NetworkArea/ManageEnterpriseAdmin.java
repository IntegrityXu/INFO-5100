    public void populateTbl() {
        int rowCount = userTbl.getRowCount();
        DefaultTableModel model = (DefaultTableModel) userTbl.getModel();
        for (int i = rowCount - 1; i >= 0; i --) {
            model.removeRow(i);
        }
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpaiseList()) {
            for (UserAccount account : enterprise.getUserAccountDirectory().getUserAccountList()) {
            Object[] row = new Object[2];
            row[0] = account;
            row[1] = account.getRole();
            model.addRow(row);
        }
        }
        
    }

    
    public void populateCbx() {
        roleCbx.removeAllItems();
        for (Role role : network.getSupportRoles()) {
            roleCbx.addItem(role);
        }
    }
