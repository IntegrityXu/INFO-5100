    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = userTbl.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select one row first!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        UserAccount account = (UserAccount) userTbl.getValueAt(selectRow, 0);
        network.getUserAccountDirectory().RemoveUserAccount(account);
        JOptionPane.showMessageDialog(null, "Delete account successfully!");
        populateTbl();
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if (nameTxt.getText().equals("") || pwdTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "User Name and Password can't be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Role role = (Role) roleCbx.getSelectedItem();
        String name = nameTxt.getText();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpaiseList()) {
            for (UserAccount account : enterprise.getUserAccountDirectory().getUserAccountList()) {
                if (account.getUserName().equals(name)) {
                    JOptionPane.showMessageDialog(null, "This Account has exist!", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
        }
        UserAccount account = new UserAccount(name, pwdTxt.getText(), role);
        Enterprise enterprise = null;
        for (Enterprise ep : network.getEnterpriseDirectory().getEnterpaiseList()) {
            if (ep.getSelfRole().equals(role)) {
                enterprise = ep;
                break;
            }
        }
        if (enterprise == null) {
            JOptionPane.showMessageDialog(null, "There is no such enterprise this role can manage!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        enterprise.getUserAccountDirectory().AddUserAccount(account);
        JOptionPane.showMessageDialog(null, "Create account successfully!");
        populateTbl();
    }//GEN-LAST:event_createBtnActionPerformed


