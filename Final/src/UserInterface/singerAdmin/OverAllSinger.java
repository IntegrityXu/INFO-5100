    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        if (SearchTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please input a singer name!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
            
        Singer singer = singerDirectory.findSinger(SearchTxt.getText());
        if (singer == null) {
            JOptionPane.showMessageDialog(null, "No such a singer!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ViewSingerDetail vsd = new ViewSingerDetail(container, singer);
        container.add("VIewSingerDetail", vsd);
        CardLayout layout = (CardLayout)container.getLayout();
        layout.next(container);
        SearchTxt.setText("");
    }//GEN-LAST:event_SearchBtnActionPerformed

