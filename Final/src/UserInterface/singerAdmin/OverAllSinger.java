    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int row = singerTbl.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Singer singer = (Singer)singerTbl.getValueAt(row, 1);
        singerDirectory.RemoveSinger(singer.getId());
        for (Organization og : musicPlatEnterprise.getOrganizationDirectory().getOrganizationList()) {
            if (og instanceof SongOrganization) {
                Iterator <Song> iterator = og.getSongDirectory().getSongList().iterator();
                while (iterator.hasNext()) {
                Song song = iterator.next();
                if (song.getSingerid() == singer.getId()) {
                    iterator.remove();
                }
            }
                break;
            }
        }
        refreshTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

