     public void refreshTable() {
        int rowCount = singerTbl.getRowCount();
        DefaultTableModel model = (DefaultTableModel)singerTbl.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Singer singer:singerDirectory.getSingerList()) {
            Object row[] = new Object[2];
            row[0] = singer.getId();//why
            row[1]=singer;
           // row[2]=s.getSinger();       
            model.addRow(row);
        }
    }

