    private void viewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = flightTbl.getSelectedRow();
        if ( selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else {
            Flight fl = (Flight) flightTbl.getValueAt(selectedRow, 0);
            ViewFlightJPanel vfJP = new ViewFlightJPanel(fl, container, airline);
            container.add("ViewFlightJPanel",vfJP);
            CardLayout layout = (CardLayout) container.getLayout();
            layout.next(container);
        }
    }//GEN-LAST:event_viewDetailsBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        if ( findTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please input a Flight ID number for search");
            return;
        }
        try {
            Integer.parseInt(findTextField.getText());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please input valid data!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int flightID = Integer.parseInt(findTextField.getText());
        Flight fl = airline.searchFlight(flightID);
        
        if (fl == null) {
            JOptionPane.showMessageDialog(null, "Oops, No such a fligt!");
            return;
        }
            
        ViewFlightJPanel vfJP = new ViewFlightJPanel(fl, container, airline);
        container.add("ViewFlightJPanel",vfJP);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_searchBtnActionPerformed


