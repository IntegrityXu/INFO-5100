    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = flightTbl.getSelectedRow();
        if( selectedRow < 0 ) 
            JOptionPane.showMessageDialog(null, "Please select a row from the table first!","Warning", JOptionPane.WARNING_MESSAGE);
        else {
            int result = JOptionPane.showConfirmDialog(null, "Would you like to delete this flight?", "warning", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION) {
                Flight fl = (Flight) flightTbl.getValueAt(selectedRow, 0);
                airline.DeleteFlight(fl);
                PopulateTable();
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

