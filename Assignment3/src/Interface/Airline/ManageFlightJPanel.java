    public void PopulateTable () {
        DefaultTableModel dtm = (DefaultTableModel) flightTbl.getModel();
        dtm.setRowCount(0);
        for ( Flight fl : airline.getFlightSchedule()) {
            Object [] row = new Object[3];
            row[0] = fl;
            row[1] = fl.getDeparture();
            row[2] = fl.getDestination();
            dtm.addRow(row);
        }
    }

