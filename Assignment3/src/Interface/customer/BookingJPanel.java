    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null, "Are you sure to book this Flight?", "Warning", JOptionPane.YES_NO_OPTION);
        if ( result == JOptionPane.YES_OPTION ) {
            if ( userIDTextField.getText().isEmpty() || nameTextField.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter all field!");
                return;
            }
            try {
                Integer.parseInt(userIDTextField.getText());
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, "the userID must be number !!" , "Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            Customer cu = travelagency.AddCostumer();
            cu.setFlight(flight);
            cu.setName(nameTextField.getText());
            cu.setUserID(Integer.parseInt(userIDTextField.getText()));
            String seatString = (String) seatBox.getSelectedItem();
            int[] seatl = new int[2];
            char c1 = seatString.charAt(1);
            seatl[0] = seatString.charAt(1) - '0' ;
            seatl[1] = seatString.charAt(3) - '0' ;
            flight.getSeat().setLocation(seatl[0] - 1, seatl[1] - 1);
            cu.setSeat(seatl);
            
            userIDTextField.setEditable(false);
            nameTextField.setEditable(false);
            seatBox.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Book Flight seuccessfully!");
        }
    }//GEN-LAST:event_bookBtnActionPerformed


