    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = airlinesTbl.getSelectedRow();
        if( selectRow >= 0) {
            int result = JOptionPane.showConfirmDialog(null, "Would you like to delete the airline??", "warniong", JOptionPane.YES_NO_OPTION);
            if( result == JOptionPane.YES_OPTION ) {
                AirLine al = (AirLine) airlinesTbl.getValueAt(selectRow, 0);
                travelAgency.DeleteAirLine(al);
                PopulateTable();
            }
        }
        else 
            JOptionPane.showMessageDialog(null, "Please select a row from the table first","Warning", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = airlinesTbl.getSelectedRow();
        if( selectRow >= 0) {
            
            AirLine al = (AirLine)airlinesTbl.getValueAt(selectRow, 0);
            ViewAirLineJPanel valJP = new ViewAirLineJPanel(al, container, travelAgency );
            container.add("ViewAirLineJPanel", valJP);
            CardLayout layout = (CardLayout) container.getLayout();
            layout.next(container);
        
        }
        else 
            JOptionPane.showMessageDialog(null, "Please select a row from the table first!", "Warning", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_viewBtnActionPerformed


