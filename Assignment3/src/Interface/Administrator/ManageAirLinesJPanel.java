    public void PopulateTable () {
        
        DefaultTableModel dtm = (DefaultTableModel) airlinesTbl.getModel();
        dtm.setRowCount(0);
        for (AirLine al : travelAgency.getAirlineList()) {
            Object row[] = new Object[2];
            row[0] = al;
            row[1] = al.FleetNumber();
            dtm.addRow(row);
        }
        
    }

