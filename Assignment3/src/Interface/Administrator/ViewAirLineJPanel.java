    public void Display() {
        airlineNameTextField.setText(airline.getName());
        quantityTextField.setText(String.valueOf(airline.FleetNumber()));
        
        DefaultTableModel dtm = (DefaultTableModel) flightScheduleTbl.getModel();
        dtm.setRowCount(0);
        for (Flight fl : airline.getFlightSchedule()) {
            Object [] row = new Object[3];
            row[0] = fl;
            row[1] = fl.getDeparture();
            row[2] = fl.getDestination();
            dtm.addRow(row);
        }
        
        DefaultTableModel dtmf = (DefaultTableModel) fleetTbl.getModel();
        dtmf.setRowCount(0);
        for ( Airplane ap : airline.getFleet()) {
            Object[] row = new Object[2];
            row[0] = ap;
            row[1] = ap.getModel();
            dtmf.addRow(row);
        }
        
    }

