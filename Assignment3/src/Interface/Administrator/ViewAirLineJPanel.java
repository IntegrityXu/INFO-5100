    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if(airlineNameTextField.getText().isEmpty() || quantityTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all text Fields!!","warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (travelagency.SearchAirLine(airlineNameTextField.getText()) != null) {
            JOptionPane.showMessageDialog(null, "Each ailine must have an unique name! ", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } 
            
        airline.setName(airlineNameTextField.getText());
        JOptionPane.showMessageDialog(null, "update airline information successfully!");
        saveBtn.setEnabled(false);
        updateBtn.setEnabled(true);
        airlineNameTextField.setEnabled(false);
        airlineNameTextField.setEditable(false);
    }//GEN-LAST:event_saveBtnActionPerformed

